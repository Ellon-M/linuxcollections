#! /bin/sh
#                         SOME USEFUL SHELL COMMANDS  
#         _______________________________________________________________

#DATE:
date

#TIME:
time

#LOGGED IN USERS:
who

#CURRENT USER:
whoami

#CALENDAR:
cal

#PRINT WORKING DIRECTORY
pwd

#CREATE A NEW DIRECTORY
mkdir dummydirectory

#CREATE A NEW EMPTY FILE
touch dummyfile

#PRINT FILES AND DIRECTORY OF THE CURRENT DIRECTORY
ls

#REMOVE A FILE
rm dummyfile

#REMOVE A DIRECTORY
rmdir dummydirectory

#ROOT USER
# prompt

#SIMPLE USER
$ prompt

#SWITCH FROM ROOT TO SIMPLE USER
exit

#SWITCH FROM SIMPLE USER TO ROOT USER
sudo -i


#__OTHERS__

#FONT SIZE
`ctrl, shift and + or  -
#CLEAR TERMINAL
`clear` or `Ctrl + l`
#EXIT SESSION
`exit`





